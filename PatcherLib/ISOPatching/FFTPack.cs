        private static void GetFFTPackResources()
        {
            using( MemoryStream memStream = new MemoryStream( PatcherLib.Properties.Resources.Resources_tar, false ) )
            using( GZipInputStream gzStream = new GZipInputStream( memStream ) )
            using ( TarInputStream tarStream = new TarInputStream( gzStream ) )
            {
                TarEntry entry = tarStream.GetNextEntry();
                while ( entry != null )
                {
                    if ( entry.Name == "FFTPackFiles.xml" )
                    {
                        XmlDocument doc = new XmlDocument();
                        doc.Load( tarStream );
                        resourcesDoc = doc;
                    }
                    entry = tarStream.GetNextEntry();
                }
            }
        }

        private static Dictionary<int, string> fftPackFiles;

        public static Dictionary<int, string> FFTPackFiles
        {
            get
            {
                if (fftPackFiles == null)
                {
                    try
                    {
                        fftPackFiles = new Dictionary<int, string>();

                        XmlNodeList nodes = resourcesDoc.SelectNodes("/files/file");
                        foreach (XmlNode node in nodes)
                        {
                            int key = Convert.ToInt32(node.Attributes["entry"].InnerText);
                            if (fftPackFiles.ContainsKey(key))
                            {
                                throw new Exception();
                            }
                            fftPackFiles.Add(Convert.ToInt32(node.Attributes["entry"].InnerText), node.Attributes["name"].InnerText);
                        }
                    }
                    catch (Exception)
                    {
                        fftPackFiles = null;
                        throw;
                    }
                }

                return fftPackFiles;
            }
        }


        public static void DumpToDirectory( string filename, string path, BackgroundWorker worker )
        {
            FileStream stream = null;

            try
            {
                stream = new FileStream( filename, FileMode.Open, FileAccess.Read );
                DumpToDirectory(stream, path, stream.Length, worker);
            }
            catch( Exception )
            {
                throw;
            }
            finally
            {
                if( stream != null )
                {
                    stream.Close();
                }
            }
        }

        public static void DumpToDirectory( Stream stream, string path, long fftpackLength, BackgroundWorker worker )
        {
            MakeDirectories( path, "BATTLE", "EFFECT", "EVENT", "MAP", "MENU", "SOUND", "WORLD", "SAVEIMAGE", "unknown", "OPEN", "WOTL" );
            for( int i = 1; i <= NumFftPackFiles; i++ )
            {
                byte[] bytes = GetFile( stream, i, fftpackLength );
                string filename = string.Empty;

                if( FFTPackFiles.ContainsKey( i ) )
                {
                    filename = FFTPackFiles[i];
                }
                else if( bytes.Length == 0 )
                {
                    filename = string.Format( "unknown/fftpack.{0}.dummy", i );
                }
                else
                {
                    filename = string.Format( "unknown/fftpack.{0}", i );
                }

                if (worker != null)
                {
                    worker.ReportProgress((i * 100) / NumFftPackFiles, string.Format("Extracting {0}", filename));
                }

                filename = Path.Combine( path, filename );

                SaveToFile( bytes, filename );

            }
        }

        public static byte[] GetFile( Stream stream, int index, long fftPackLength )
