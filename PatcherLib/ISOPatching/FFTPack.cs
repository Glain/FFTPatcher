        private static void MakeDirectories( string path, params string[] dirs )
        {
            foreach( string dir in dirs )
            {
                Directory.CreateDirectory( Path.Combine( path, dir ) );
            }
        }

        private static byte[] ReadFile( string path, int index )
        {
            FileStream stream = null;

            try
            {
                if( FFTPackFiles.ContainsKey( index ) && File.Exists( Path.Combine( path, FFTPackFiles[index] ) ) )
                {
                    stream = new FileStream( Path.Combine( path, FFTPackFiles[index] ), FileMode.Open );
                }
                else if( File.Exists( Path.Combine( path, string.Format( "unknown/fftpack.{0}", index ) ) ) )
                {
                    stream = new FileStream( Path.Combine( path, string.Format( "unknown/fftpack.{0}", index ) ), FileMode.Open );
                }
                else if( File.Exists( Path.Combine( path, string.Format( "unknown/fftpack.{0}.dummy", index ) ) ) )
                {
                    return new byte[0];
                }
                else
                {
                    string file = "";
                    FFTPackFiles.TryGetValue(index, out file);

                    if (string.IsNullOrEmpty(file))
                        throw new Exception("Could not load file for index: " + index);
                    else
                        throw new Exception("Could not load file: " + FFTPackFiles[index]);
                }

                byte[] result = new byte[stream.Length];
                stream.Read( result, 0, (int)stream.Length );

                return result;
            }
            catch( Exception )
            {
                throw;
            }
            finally
            {
                if( stream != null )
                {
                    stream.Close();
                    stream = null;
                }
            }
        }

        private static void SaveToFile( byte[] bytes, string path )
        {
            FileStream stream = null;
            try
            {
                stream = new FileStream( path, FileMode.Create );
                stream.Write( bytes, 0, bytes.Length );
            }
            catch( Exception )
            {
                throw;
            }
            finally
            {
                if( stream != null )
                {
                    stream.Flush();
                    stream.Close();
                }
            }
        }

